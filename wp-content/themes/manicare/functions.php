	wp_enqueue_script( 'small-menu', get_template_directory_uri() . '/js/small-menu.js', array( 'jquery' ), '20120206', true );
	wp_enqueue_script( 'homepage', get_template_directory_uri() . '/js/homepage.js', array( 'jquery' ));

